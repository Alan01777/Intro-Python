'''Faça um programa que leia dois valores numéricos e realize a soma, subtração,
multiplicação e divisão deles.'''

num1=int(input('Valor 1:'))
num2=int(input('Valor 2:'))

soma=num1+num2
div=num1/num2
mult=num1*num2
sub=num1-num2


print(f'Soma: {soma}\n'
      f'Subtração: {sub}\n'
      f'Multiplicação: {mult}\n'
      f'Divisão: {div}\n')